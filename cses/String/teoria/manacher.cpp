    string s;
    cin >> s;
    vi d = manacher(s);
    int n = d.size();
    int len =0, ans, idx;
    for(int i = 0; i < n; i++){
        if(d[i] > len){
            len = d[i];
            ans = len-1;
            idx = i/2;
        }
    }
    cout << s.substr(idx - ans/2, ans) << endl;
